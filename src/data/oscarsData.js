// Agregar esta función para guardar en localStorage
export const savePredictionsToStorage = (predictions, userId) => {
  if (!userId) return; // No guardar si no hay usuario
  localStorage.setItem(`oscarPredictions_${userId}`, JSON.stringify(predictions));
};

// Agregar esta función para cargar desde localStorage
export const loadPredictionsFromStorage = (userId) => {
  if (!userId) return null; // No cargar si no hay usuario
  const saved = localStorage.getItem(`oscarPredictions_${userId}`);
  return saved ? JSON.parse(saved) : null;
}; 